vector<int> dp;

int fibonacciDP(int n) {
    if (n < 3) return 1;
    if (dp[n] != -1) return dp[n];
    return dp[n] = fibonacciDP(n-1) + fibonacciDP(n-2) + fibonacciDP(n-3);
}

